		}
	}
	
	return $readBits;
}

function parseRunewordBits(&$item) {
	global $runewordNames;
	$runewordID = reverseBits(ReadBits64(12, true), 12);
	$item['RunewordID'] = $runewordID;
	$item['RunewordName'] = $runewordNames[$runewordID];
	reverseBits(ReadBits64(4, true), 4);
}

function parseMagicalList() {
	
	global $magicalProperties;
	$magicAttributes = array();
	$readBits = 0;

	while(true) {
		
		$id = reverseBits(ReadBits64(9, true), 9);
		$readBits += 9;
		
		if($id == 0x1ff)
			break;
		
		$prop = $magicalProperties[$id];
		if(!$prop)
			exit(json_encode(array('error' => "Unknown magical property id: $id")));
		
		$values = array();
		foreach($prop['Bits'] as $bits) {
			$val = reverseBits(ReadBits64($bits, true), $bits);
			$readBits += $bits;
			if(isset($prop['Bias']) && $prop['Bias'] != 0)
				$val = $val - $prop['Bias'];
			array_push($values, $val); 
		}
		$attr = array('ID' => $id, 'Name' => $prop['Name'], 'Values' => $values);
		array_push($magicAttributes, $attr);
	}
	return array($magicAttributes, $readBits);
}

