	$readBits += 3;
	ReadBits64(1, true);
	$readBits ++;
	$item['AltPositionID'] = $positionMap[reverseBits(ReadBits64(3, true), 3)];
	$readBits += 3;
	if($item['IsEar'] == 0) {
		$itemType = '';
		for($j=0;$j<4;$j++)
			$itemType .= chr(reverseBits(ReadBits64(8, true), 8));
		$item['Type'] = trim($itemType);
		$item['TypeID'] = getTypeID($item['Type']);
		switch($item['TypeID']) {
			case 'armor':
