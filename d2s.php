				break;
		}
		$item['typeName'] = $typeName;
		$item['NrOfItemsInSockets'] = reverseBits(ReadBits64(3, true), 3);
	}
	else {
		$earClass = reverseBits(ReadBits64(3, true), 3);
		$earLevel = reverseBits(ReadBits64(7, true), 7);
		$name = '';
		while(true) {
			$c = reverseBits(ReadBits64(7, true), 7);
			if($c == 0)
				break;
