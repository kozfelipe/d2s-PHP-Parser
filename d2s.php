function dec2bin($decimal, $length=8) {
	return str_pad(decbin($decimal), $length, 0, STR_PAD_LEFT);
}

function reverseByte($decimal, $lenght=8) { 
	$bits = str_split(dec2bin($decimal, $lenght));
	$result = array();
	foreach($bits as $i => $bit)
		$result[(count($bits) - $i - 1)] = $bit;
	$output = '';
	for($i=0;$i<count($result);$i++)
		$output .= $result[$i];
	return bindec($output);
}

function reverseBits($binary, $lenght=8) {
	$d = 0;
	for($i=0;$i<$lenght;$i++) {
		$d <<= 1;
		$d |= $binary & 1;
		$binary >>= 1;
	}
	return $d;
}

function ReadBits64($lenght, $reverse) {
	global $pointer;
	global $buffer;
	while($lenght > $pointer['bits']) {
		$b = $buffer[$pointer['index']];	
		$pointer['index']++;
		if($reverse)
			$b = reverseByte($b);	
		$pointer['n'] <<= 8;
		$pointer['n'] |= $b;
		$pointer['bits'] += 8;
	}
	$n = ($pointer['n'] >> ($pointer['bits'] - $lenght)) & ((1 << $lenght) - 1);
	$pointer['bits'] -= $lenght;
	return $n;
}

