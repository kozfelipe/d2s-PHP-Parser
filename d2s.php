		}
		$item['EarAttributes'] = array(
			'Class' => $earClass,
			'Level' => $earLevel,
			'Name' =>  $name,
		);
		$remainder = $readBits % 8;
		if($remainder > 0) {
			$bitsToAlign = 8 - $remainder;
			reverseBits(ReadBits64($bitsToAlign, true), $bitsToAlign);
		}
	}

}

function parseRareOrCraftedBits(&$item) {
	
	global $rareNames;
	$readBits = 0;
	
