	$readBits ++;
	ReadBits64(15, true);
	$readBits += 15;
	$item['LocationID'] = $locationMap[reverseBits(ReadBits64(3, true), 3)];
	$readBits += 3;
	$item['EquippedID'] = reverseBits(ReadBits64(4, true), 4);
	$readBits += 4;
	$item['PositionX'] = reverseBits(ReadBits64(4, true), 4);
	$readBits += 4;
