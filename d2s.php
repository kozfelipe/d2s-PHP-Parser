function parseItemList() {
	
	global $buffer;
	global $pointer;
	global $char;
	global $locationMap;
	global $socketablesWeapons;
	global $socketablesArmor;
	global $socketablesShields;
	global $setListMap;
	global $qualityMap;
	global $quantityMap;
	global $magicalPrefixes;
	global $magicalSuffixes;
	global $setNames;
	global $uniqueNames;
	global $tomeMap;
	global $setReqIDsMap;
	
	# start item section
	if( chr($buffer[$pointer['index']]) != 'J' || chr($buffer[$pointer['index']+1]) != 'M')
		exit(json_encode(array('error' => 'Failed to find the items header')));
	else
		$pointer['index'] += 2;

	$itemcount = $buffer[$pointer['index']] + $buffer[$pointer['index']+1];
	$pointer['index'] += 2;

	$pointer['n'] = 0;
	$pointer['bits'] = 0;
	$itemList = array();

	for($i=0;$i<$itemcount;$i++) {
		
		$readBits = 0;
		$parsed = array();
		
		parseSimpleBits($parsed);
		
		$readBits += 111;	
		if($parsed['SimpleItem'] == 0) {
			$parsed['ID'] = reverseBits(ReadBits64(32, true), 32);
			$readBits += 32;
			$parsed['Level'] = reverseBits(ReadBits64(7, true), 7);
			$readBits += 7;
			$quality = reverseBits(ReadBits64(4, true), 4);
			$readBits += 4;
			$parsed['Quality'] = $qualityMap[$quality];
			$parsed['MultiplePictures'] = reverseBits(ReadBits64(1, true), 1);
			$readBits++;
			if($parsed['MultiplePictures'] == 1) {
				$parsed['PictureID'] = reverseBits(ReadBits64(3, true), 3);
				$readBits += 3;
			}
			$parsed['ClassSpecific'] = reverseBits(ReadBits64(1, true), 1) == 1 ? true : false;
			$readBits++;
			if($parsed['ClassSpecific'] == true) {
				reverseBits(ReadBits64(11, true), 11);
				$readBits += 11;
			}
			switch($parsed['Quality']) {
				case 'lowQuality':
					$parsed['LowQualityID'] = reverseBits(ReadBits64(3, true), 3);
					$readBits += 3;
					break;
				case 'normal':
					break;
				case 'highQuality':
					reverseBits(ReadBits64(3, true), 3);
					$readBits += 3;
					break;
				case 'magicallyEnhanced':
					$parsed['MagicPrefix'] = reverseBits(ReadBits64(11, true), 11);
					$parsed['MagicPrefixName'] = $magicalPrefixes[$parsed['MagicPrefix']];
					$parsed['MagicSuffix'] = reverseBits(ReadBits64(11, true), 11);
					$parsed['MagicSuffixName'] = $magicalSuffixes[$parsed['MagicSuffix']];
					$readBits += 22;
					break;
				case 'partOfSet':
					$parsed['SetID'] = reverseBits(ReadBits64(12, true), 12);
					$parsed['setName'] = $setNames[$parsed['SetID']];
					$readBits += 12;
					break;
				case 'rare':
				case 'crafted':
					$rBits = parseRareOrCraftedBits($parsed);
					$readBits += $rBits;
					break;
				case 'unique':
					$parsed['UniqueID'] = reverseBits(ReadBits64(12, true), 12);
					$parsed['uniqueName'] = $uniqueNames[$parsed['UniqueID']];
					$readBits += 12;
					break;
			}
		
		
			if($parsed['GivenRuneword'] == 1) {
				parseRunewordBits($parsed);
				$readBits += 16;
			}
			
			if($parsed['Personalized'] == true) {
				$name = '';
				while(true) {
					$c = reverseBits(ReadBits64(7, true), 7);
					if($c == 0)
						break;
					$name .= chr($c);
				}
				$parsed['PersonalizedName'] = $name;
			}
			
			if(isset($tomeMap[$parsed['Type']])) {
				reverseBits(ReadBits64(5, true), 5);
				$readBits += 5;
			}
			
			//$parsed['Timestamp'] = reverseBits(ReadBits64(1, true), 1);
			reverseBits(ReadBits64(1, true), 1);
			$readBits++;
			
			if($parsed['TypeID'] == 'armor' || $parsed['TypeID'] == 'shield') {
				$defRating = reverseBits(ReadBits64(11, true), 11);
				$readBits += 11;
				$parsed['DefenseRating'] = $defRating - 10;
			}

			if($parsed['TypeID'] == 'armor' || $parsed['TypeID'] == 'shield' || $parsed['TypeID'] == 'weapon') {
				$parsed['MaxDurability'] = reverseBits(ReadBits64(8, true), 8);
				$readBits += 8;
				if($parsed['MaxDurability'] > 0) {
					$parsed['CurrentDurability'] = reverseBits(ReadBits64(8, true), 8);
					reverseBits(ReadBits64(1, true), 1);
					$readBits += 9;
				}
			}
			
			if(isset($quantityMap[$parsed['Type']])) {
				$parsed['Quantity'] = reverseBits(ReadBits64(9, true), 9);
				$readBits += 9;
			}
			
			if($parsed['Socketed'] == true) {
				$parsed['TotalNrOfSockets'] = reverseBits(ReadBits64(4, true), 4);
				$readBits += 4;
			}
			
			$setListValue = 0;
			if($parsed['Quality'] == 'partOfSet') {
				$setListValue = reverseBits(ReadBits64(5, true), 5);
				$readBits += 5;
				$parsed['SetListCount'] = $setListMap[$setListValue];
				if($parsed['SetListCount'] > 0) {
					$parsed['SetAttributes'] = array();
					for($i=0;$i<$parsed['SetListCount'];$i++) {
						$MagicalList = parseMagicalList();
						$readBits += $MagicalList[1];
						array_push($parsed['SetAttributes'], $MagicalList[0]);
					}
					$reqSetIDs = $setReqIDsMap[$parsed['SetID']];
					if($reqSetIDs)
						$parsed['SetAttributesIDsReq'] = $reqSetIDs;
					else {
						$parsed['SetAttributesNumReq'] = array();
						for($i=0;$i<5;$i++) {
							if($setListValue & (1 << $i) == 0)
								continue;
							$parsed['SetAttributesNumReq'] = $reqSetIDs;
						}
						array_push($parsed['SetAttributesNumReq'], $i+2);
					}
				}
			}
			
			$MagicalList = parseMagicalList();
			$readBits += $MagicalList[1];
			$parsed['MagicAttributes'] = $MagicalList[0];

			if($parsed['GivenRuneword'] == 1) {
				$MagicalList = parseMagicalList();
				$readBits += $MagicalList[1];
				$parsed['RunewordAttributes'] = $MagicalList[0];
			}
			
		}
		
		if($locationMap[$parsed['LocationID']] == 'socketed') {
			$last = count($itemList) - 1;
			
			if($itemList[$last]['TypeID'] == 'weapon' && isset($socketablesWeapons[$parsed['Type']])) 
				$parsed['MagicAttributes'] = $socketablesWeapons[$parsed['Type']];
			
			if($itemList[$last]['TypeID'] == 'armor' && isset($socketablesArmor[$parsed['Type']])) 
				$parsed['MagicAttributes'] = $socketablesArmor[$parsed['Type']];
			
			if($itemList[$last]['TypeID'] == 'shield' && isset($socketablesShields[$parsed['Type']])) 
				$parsed['MagicAttributes'] = $socketablesShields[$parsed['Type']];
			
			$itemList[$last]['SocketedItems'] = array();
			array_push($itemList[$last]['SocketedItems'], $parsed);
		}
		else {
			if($parsed['NrOfItemsInSockets'] > 0 && $parsed['SimpleItem'] == 0)
				$itemcount += $parsed['NrOfItemsInSockets'];
			array_push($itemList, $parsed);
		}
		
		$remainder = $readBits % 8;
		if($remainder > 0) {
			$bitsToAlign = 8 - $remainder;
			reverseBits(ReadBits64($bitsToAlign, true), $bitsToAlign);
			$pointer['n'] = 0;
			$pointer['bits'] = 0;
		}

	}
	
	$char['itens'] = $itemList;
	
}

