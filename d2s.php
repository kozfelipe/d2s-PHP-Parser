	$readBits += 3;
	$item['SimpleItem'] = reverseBits(ReadBits64(1, true), 1);
	$readBits ++;
	$item['Ethereal'] = reverseBits(ReadBits64(1, true), 1) == 1 ? true : false;
	$readBits ++;
	ReadBits64(1, true);
	$readBits ++;
